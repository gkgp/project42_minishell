char	*ft_strcat(char *dest, char *src)
{
	char	*pt_dest;

	pt_dest = dest;
	while (*dest != '\0')
		dest++;
	while (*src != '\0')
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\0';
	return (pt_dest);
}
